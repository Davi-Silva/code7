import mongoose from 'mongoose';
import { IAdmin } from '@interfaces/models/admins';

const AdminSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  created_at: {
    type: Date,
    required: true,
  },
  updated_at: {
    type: Date,
    required: false,
  },
});

const Admin = mongoose.model<IAdmin>('Admin', AdminSchema);

export default Admin;
